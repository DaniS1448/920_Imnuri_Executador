int addRegistru(String entrada){
	FILE *fp = fopen(".Registru_Imnuri.txt", "ab+");
	
	char username[UNLEN+1];
	DWORD username_len = UNLEN+1;
	GetUserName(username, &username_len);
	
	time_t     now;
    struct tm *ts;
    char       buf[80];
	/* Obtener la hora actual */
    now = time(0);

    /* Formatear e imprimir el tiempo, "ddd yyyy-mm-dd hh:mm:ss zzz" */
    ts = localtime(&now);
    strftime(buf, sizeof(buf), "%Y-%m-%d %H:%M:%S", ts);
	
	fprintf(fp, "\r\n%s - %s | IMN: %s\n", username, buf, entrada);
	fclose(fp);
}
	
int esNumero (String entrada){
	for (int i=0; i<strlen(entrada)-1; i++){
		if (!(entrada[i]>='0' && entrada[i]<='9')) return 0;
	}
	return 1;
}

int abrirNumero(int n){
	char ruta[3];
	char comandoFinal[26];
	char comando[]="start powerpnt ";
	char extension[]=".pptx";
	sprintf(ruta,"%03d", n);
	strcat(comandoFinal,comando);
	strcat(comandoFinal,ruta);
	strcat(comandoFinal,extension);
	if (n>0 && n<=920)	system(comandoFinal);
	else printf("\n	EROARE! Imnul trebuie sa fie intre 1 si 920.\n");
}

int buscarTitulo(String *nombres, String entrada){
	char cls[]="cls";
	char tmpNombres[100];
	int contador=0;
	entrada[strlen(entrada)-1]='\0';
	for(int i = 0; entrada[i]; i++){
		entrada[i] = tolower(entrada[i]);
	}
	if (strlen(entrada)==1) {
		printf("\n	Eroare! Introdu mai mult de o litera pentru a cauta un imn.\n");
		return 0;
	}
	if(strcmp(entrada, "clear") == 0) {
		system(cls);
		printf("%s", titluProgram);
		return 0;
	}
	if(strcmp(entrada, "exit") == 0) exit(0);
	if(strcmp(entrada, "help") == 0) {
		printf("\n	===COMENZI DISPONIBILE===\n\n");
		printf("	help	Ajutor\n	clear	Sterge textul scris din program\n	exit	Inchide programul\n\n");
	}
	else{
		for (int i=0; i<920; i++) {
			strcpy(tmpNombres, nombres[i]);
			for(int i = 0; tmpNombres[i]; i++) tmpNombres[i] = tolower(tmpNombres[i]);
			if(strstr(tmpNombres,entrada) != NULL) contador++;
		}
		if(strcmp(entrada, "clear")!=0) printf("\n	====== %d IMNURI GASITE ======\n\n", contador);
		for (int i=0; i<920; i++) {
			strcpy(tmpNombres, nombres[i]);
			for(int i = 0; tmpNombres[i]; i++) tmpNombres[i] = tolower(tmpNombres[i]);
			if(strstr(tmpNombres,entrada) != NULL) printf("	%s\n", nombres[i]);
		}
		if(contador==0 && (strcmp(entrada, "clear")!=0)) printf("	Nu s-a gasit nici un rezultat.\n");
		return 0;
	}
	
}

